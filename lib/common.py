# convert text 
def convert_text(val: str):
    """convert text
    - không dấu
    - white space to _

    Args:
        val (str): value truyền vào để convert 

    Returns:
        (str): value kết quả 
    """
    val = remove_accents(val)
    val = val.replace(" ", "_")
    return val
